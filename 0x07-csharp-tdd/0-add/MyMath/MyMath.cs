using System;

namespace MyMath
{
    /// <summary>Define some basics mathematics operations</summary>
    public class Operations
    {
        /// <summary>Do an Addition a + b</summary>
        /// <param name="a">The first value of the addition</param>
        /// <param name="b">The second value of the addition</param>
        /// <returns>The result of a + b</returns>
        public static int Add(int a, int b)
        {
            return a + b;
        }
    }
}
